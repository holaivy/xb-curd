package holaivy.web.simple.xb.curd;

public class XbException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 311086577410999385L;

	public XbException() {
		super();

	}

	public XbException(String message, Throwable cause) {
		super(message, cause);

	}

	public XbException(String message) {
		super(message);

	}

	public XbException(Throwable cause) {
		super(cause);

	}

}
